package com.entity;

public class Goods {
	private int gid;
	private int gcid;
	private int gchid;
	private double gprice;
	private String gpic;
	private String gbrand;
	private String gname;
	private int gstock;
	private int count;
	private int sum;
	public int getGid() {
		return gid;
	}
	public void setGid(int gid) {
		this.gid = gid;
	}
	public int getGcid() {
		return gcid;
	}
	public void setGcid(int gcid) {
		this.gcid = gcid;
	}
	public int getGchid() {
		return gchid;
	}
	public void setGchid(int gchid) {
		this.gchid = gchid;
	}
	public double getGprice() {
		return gprice;
	}
	public void setGprice(double gprice) {
		this.gprice = gprice;
	}

	public String getGpic() {
		return gpic;
	}
	public void setGpic(String gpic) {
		this.gpic = gpic;
	}
	public String getGbrand() {
		return gbrand;
	}
	public void setGbrand(String gbrand) {
		this.gbrand = gbrand;
	}
	public String getGname() {
		return gname;
	}
	public void setGname(String gname) {
		this.gname = gname;
	}
	public int getGstock() {
		return gstock;
	}
	public void setGstock(int gstock) {
		this.gstock = gstock;
	}
	
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
	public int getSum() {
		return sum;
	}
	public void setSum(int sum) {
		this.sum = sum;
	}
	public Goods(int gid, int gcid, int gchid, double gprice, String gpic,
			String gbrand, String gname, int gstock) {
		super();
		this.gid = gid;
		this.gcid = gcid;
		this.gchid = gchid;
		this.gprice = gprice;
		this.gpic = gpic;
		this.gbrand = gbrand;
		this.gname = gname;
		this.gstock = gstock;
	}
	public Goods(int gid, double gprice, String gpic, String gbrand,
			String gname, int count) {
		super();
		this.gid = gid;
		this.gprice = gprice;
		this.gpic = gpic;
		this.gbrand = gbrand;
		this.gname = gname;
		this.count = count;
	}
	public double getMoney(){
		return gprice*count;
	}
	public Goods(int gid, double gprice, String gpic, String gname) {
		super();
		this.gid = gid;
		this.gprice = gprice;
		this.gpic = gpic;
		this.gname = gname;
	}
	public Goods(int gid, double gprice, String gpic, String gname, int sum) {
		super();
		this.gid = gid;
		this.gprice = gprice;
		this.gpic = gpic;
		this.gname = gname;
		this.sum = sum;
	}
	
}

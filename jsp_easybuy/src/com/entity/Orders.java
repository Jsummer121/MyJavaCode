package com.entity;

public class Orders {
	private int oid;
	private int uid;
	private String ostatus;
	private double omoney;
	private String rname;
	private String rphone;
	private String raddr;
	public int getOid() {
		return oid;
	}
	public void setOid(int oid) {
		this.oid = oid;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public String getOstatus() {
		return ostatus;
	}
	public void setOstatus(String ostatus) {
		this.ostatus = ostatus;
	}
	public double getOmoney() {
		return omoney;
	}
	public void setOmoney(double omoney) {
		this.omoney = omoney;
	}
	public String getRname() {
		return rname;
	}
	public void setRname(String rname) {
		this.rname = rname;
	}
	public String getRphone() {
		return rphone;
	}
	public void setRphone(String rphone) {
		this.rphone = rphone;
	}
	public String getRaddr() {
		return raddr;
	}
	public void setRaddr(String raddr) {
		this.raddr = raddr;
	}
	public Orders(int oid, int uid, String ostatus, double omoney,
			String rname, String rphone, String raddr) {
		super();
		this.oid = oid;
		this.uid = uid;
		this.ostatus = ostatus;
		this.omoney = omoney;
		this.rname = rname;
		this.rphone = rphone;
		this.raddr = raddr;
	}
	
}
